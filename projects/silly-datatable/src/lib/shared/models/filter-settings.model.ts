export class FilterSettings {

  /**
   * Div container class of filter.
   */
  public formContainerClass?: string;


  /**
   * Cancel button css class.
   */
  public cancelButtonClass?: string;


  /**
   * Cancel button text ("Cancel" by default).
   */
  public cancelButtonTitle?: string;


  /**
   * Submit button class.
   */
  public submitButtonClass?: string;


  /**
   * Submit button text("Apply filters" by default).
   */
  public submitButtonTitle?: string;


  /**
   * Class of the div container that contains buttons 'Cancel' and 'Apply filters'.
   */
  public buttonsContainerClass?: string;


  /**
   * Class of the div container that contains the container with buttons and all
   * form fields.
   */
  public formElementsContainerClass?: string;
}
