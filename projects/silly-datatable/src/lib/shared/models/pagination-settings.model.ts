export class PaginationSettings {

  /**
   * Css class for customize information string "Page 1 of 10".
   */
  public infoClass: string;

  /**
   * Css class for pagination div container.
   */
  public containerClass: string;


  /**
   * Css class for buttons with arrows (start, end, next, prev).
   */
  public arrowButtonClass: string;


  /**
   * Css class for buttons with number of page.
   */
  public numberButtonClass: string;


  /**
   * "To Start" button word or symbol.
   */
  public start: string;


  /**
   * "Prev" button word or symbol.
   */
  public prev: string;


  /**
   * "Next" button word or symbol.
   */
  public next: string;


  /**
   * "To Last" button word or symbol.
   */
  public last: string;
}
